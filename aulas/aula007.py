#soma +
n = 5+2
print(f'a soma 5+2={n}')

#subtração -
n = 5-2
print(f'a subtração 5-2={n}')

#multiplicação *
n = 5*2
print(f'a multiplicação 5*2={n}')

#divisão sem resto /
n = 5/2
print(f'a divisão 5/2={n}')

#potenciação **
n = 5**2
print(f'a potenciação 5^2={n}')

#divisão inteira //
n = 5//2
print(f'a divisão com resto de 5//2={n}')

#resto da divisão %
n = 5%2
print(f'o resto da divisão de 5/2 é={n}')
